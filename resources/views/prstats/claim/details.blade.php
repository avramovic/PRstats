<p>Claiming profiles allows you to customize a player profile slightly or turn it private. Also,
profiles of the claimed players get their dynamic signature generated instantly. For non-claimed
    player profiles, signature gets generated only once they reach 250K in total score.</p>
<p>If you gave gravatar account registered with the same e-mail address, your player avatar will
    be replaced with your gravatar avatar.</p>
<p>In the future, we may provide additional features to the claimed profiles.</p>